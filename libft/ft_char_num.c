#include "libft.h"

int	ft_char_num(char *str, char c)
{
	int i;
	int n;

	i = 0;
	n = 0;
	if (!str)
		return (-1);
	while(str && str[i])
	{
		if (str[i++] == c)
			n++;
	}
	return (n);
}
